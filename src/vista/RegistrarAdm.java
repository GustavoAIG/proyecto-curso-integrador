    private void cargarIdsTiendas() {
        ConexionBD conexionBD = new ConexionBD();
        try (Connection conn = conexionBD.ConectarBaseDatos()) {
            String sql = "SELECT nom_tien FROM tiendas";
            PreparedStatement stmt = conn.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();

            cbxIdTiendaEmpleado.removeAllItems(); // Limpiar el JComboBox antes de cargar los datos
            while (rs.next()) {
                cbxIdTiendaEmpleado.addItem(rs.getString("nom_tien")); // Añadir cada ID al JComboBox
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar los IDs de tiendas: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void cargarIdsUsuarios() {
    ConexionBD conexionBD = new ConexionBD();
    try (Connection conn = conexionBD.ConectarBaseDatos()) {
        String sql = "SELECT nom_usu FROM usuarios WHERE rol_usu = 'emple'";
        PreparedStatement stmt = conn.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();

        cbxIdUsuarioEmpleado.removeAllItems(); // Limpiar el JComboBox antes de cargar los datos
        while (rs.next()) {
            cbxIdUsuarioEmpleado.addItem(rs.getString("nom_usu")); // Añadir cada ID al JComboBox
        }
        
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al cargar los IDs de usuarios: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}

    
