    private void btnRegistrarInformacionUsuarioEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarInformacionUsuarioEmpleadoActionPerformed
      // Obtener los valores de los campos de texto y ComboBox
    String nombreUsuarioEmpleado = txtNombreEmpleado.getText();
    String apellidoUsuarioEmpleado = txtApellidoEmpleado.getText();
    String idTiendaEmpleado = (String) cbxIdTiendaEmpleado.getSelectedItem(); // ID de tienda seleccionado
    String idUsuarioEmpleado = (String) cbxIdUsuarioEmpleado.getSelectedItem(); // ID de usuario seleccionado

    // Comprobar que los campos no estén vacíos
    if (nombreUsuarioEmpleado.isEmpty() || apellidoUsuarioEmpleado.isEmpty() || idTiendaEmpleado == null || idUsuarioEmpleado == null) {
        JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.", "Error", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Insertar los datos en la base de datos
    ConexionBD conexionBD = new ConexionBD();
    try (Connection conn = conexionBD.ConectarBaseDatos()) {
        String sql = "INSERT INTO empleados (nom_emp, ape_emp, id_tien, id_usu) VALUES (?, ?, ?, ?)";
        PreparedStatement stmt = conn.prepareStatement(sql);
        stmt.setString(1, nombreUsuarioEmpleado);
        stmt.setString(2, apellidoUsuarioEmpleado);
        stmt.setString(3, idTiendaEmpleado);
        stmt.setString(4, idUsuarioEmpleado);

        int filasInsertadas = stmt.executeUpdate();
        if (filasInsertadas > 0) {
            JOptionPane.showMessageDialog(this, "Empleado registrado correctamente.");
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar el empleado.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al registrar el empleado: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnRegistrarInformacionUsuarioEmpleadoActionPerformed

